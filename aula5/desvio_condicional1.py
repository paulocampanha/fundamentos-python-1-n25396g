# Nesse programa vamos estudar as formas de desvio 
# condicional. Essa estrutura executa um bloco de 
# código se uma condicional for tratada como verdadeira
# Usamos os comando: if, elif e else

# Comando: if 

idade = int(input('Digite sua idade: '))

if idade >= 18:
    print('Você é maior de idade.')
    print('Você pode tirar carta de motorista.')
    print('Você pode entrar em lugares adultos.')
print('-' * 30)
print('Fim do Programa')
