# Nesse programa vamos estudar a estrutura de repetição
# while. Esse estrutura executa um bloco de código várias
# vezes enquanto uma condição for verdadeira

contador = 1

while contador <= 10:
    print(contador)
    print('-' * 30)
    contador += 1     # contador = contador + 1
print('*' * 30)
print('Fim do Programa')