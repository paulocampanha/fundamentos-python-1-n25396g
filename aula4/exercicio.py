'''
Crie um programa que converta temperaturas de Celsius para 
Fahrenheit. A fórmula é:
F = (C * 9/5)+32.
Declare uma variável celsius e atribua a ela um valor.
Calcule a conversão e armazene o resultado em uma variável 
fahrenheit.
Imprima o resultado de forma clara, como "25°C é igual a 77°F".
'''

celsius = 25
fahrenheit = (celsius * 9/5) + 32

print(celsius,'°C é igual a',fahrenheit,'°F')
